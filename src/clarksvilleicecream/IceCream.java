    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        
               double priceContainer = 0.00,
               priceIngredient = 0.00,
               priceScoops = 0.00,
               orderTotal = 0.00;
               double numberOfScoops = 1;
        // Find out what container the customer requested
        // The price of a container depends on which one the customer selected
        
        switch (txtContainer.getText()) {
            case "Cone":
                priceContainer = 0.55;
                break;
            case "Cup":
                priceContainer = 0.75;
                break;
            default:
                priceContainer = 1.15;
                break;
             
        }
            try{
            // If the customer selected an ingredient, which is not "None", add $.95
            if (!"None".equals(txtIngredient.getText()))
                priceIngredient = 0.95;

            // Get the number of scoops
            numberOfScoops = Double.parseDouble(txtScoops.getText());

            if (numberOfScoops == 2)
                priceScoops = 2.55;
            else if (numberOfScoops == 3)
                priceScoops = 3.25;
            else
                priceScoops = 1.85;

            // Make sure the user selected a flavor, 
            // otherwise, there is no reason to process an order
            if (!"".equals(txtFlavour.getText()))
                orderTotal = priceScoops + priceContainer + priceIngredient;

            txtOrderTotal.setText(String.valueOf(orderTotal));
            btnMoneyChange.setEnabled(true);
            }catch(NumberFormatException nfe){
             JOptionPane.showMessageDialog(null, "Ingreso un valor no valido.", "Error ", JOptionPane.ERROR_MESSAGE);   
            }
    }//GEN-LAST:event_btnCalculateActionPerformed

