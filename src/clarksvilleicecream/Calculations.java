    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
           try{
            // Get the value of the total order. Actually, this value 
            // will be provided by the main form
            double TotalOrder = Double.parseDouble(txtOrderTotal.getText());
            // The amount tended will be entered by the user
	    double  AmountTended = Double.parseDouble(txtAmountTended.getText());
            // Calculate the difference of both values, assuming 
            // that the amount tended is higher
            double Difference = AmountTended - TotalOrder;
            // Display the result in the Difference text box
            txtDifference.setText(String.valueOf(Difference)); 
           }catch(NumberFormatException ex){
             JOptionPane.showMessageDialog(null, "Ingreso un valor no valido.", "Error ", JOptionPane.ERROR_MESSAGE);  
           }
    }//GEN-LAST:event_btnCalculateActionPerformed

