    private void btnsortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsortActionPerformed
        // TODO add your handling code here:
     int campo[]= new int[9]; 
     boolean flag= true;
    //Variable que contiene
     int temp=0;
     int j;
     try{
     //Lee el contenido de los textFields y lo coloca en el vector convertido a int    
     campo[0]= Integer.valueOf(txtcampo1.getText());
     campo[1]= Integer.valueOf(txtcampo2.getText());
     campo[2]= Integer.valueOf(txtcampo3.getText());
     campo[3]= Integer.valueOf(txtcampo4.getText());
     campo[4]= Integer.valueOf(txtcampo5.getText());
     campo[5]= Integer.valueOf(txtcampo6.getText());
     campo[6]= Integer.valueOf(txtcampo7.getText());
     campo[7]= Integer.valueOf(txtcampo8.getText());
     campo[8]= Integer.valueOf(txtcampo9.getText());
     
      while (flag)
     {
       //Coloca flag en false esperando cambios  
       flag= false;    
       for(j=0; j<campo.length-1;j++)
       {
        //Cambia a ordenamiento ascendente
           if (campo[j] <campo[j+1])   
        {
        //Cambia elementos    
        temp = campo[j];                
        campo[j] = campo[j+1];
        campo[j+1] = temp;
        //Muestra cambios aocurridos
        flag = true;               
         } 
        } 
         //Coloca los numeros ordenados en los campos   
         txtcamp10.setText(String.valueOf(campo[0]));  
         txtcamp11.setText(String.valueOf(campo[1]));
         txtcamp12.setText(String.valueOf(campo[2]));
         txtcamp13.setText(String.valueOf(campo[3]));
         txtcamp14.setText(String.valueOf(campo[4]));
         txtcamp15.setText(String.valueOf(campo[5]));  
         txtcamp16.setText(String.valueOf(campo[6]));
         txtcamp17.setText(String.valueOf(campo[7]));
         txtcamp18.setText(String.valueOf(campo[8]));
      } 
     }catch(NumberFormatException nfe){
       JOptionPane.showMessageDialog(null, "Ingreso un valor no valido.", "Error ", JOptionPane.ERROR_MESSAGE);  
     }
    }//GEN-LAST:event_btnsortActionPerformed

